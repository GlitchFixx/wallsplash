from typing import Final


class SEPARATOR:
    """
    This class contains constants for various separators that can be used.
    """

    PERIOD: Final[str] = "."
    COMMA: Final[str] = ","
    EMPTY_STRING: str = ""
    QUESTION_MARK: Final[str] = "?"
    SPACE: Final[str] = " "
    AMPERSAND: Final[str] = "&"
    FORWARD_SLASH: Final[str] = "/"

class Constants:
    DEQUE_MAX_LENGTH = 50